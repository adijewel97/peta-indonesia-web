CREATE TABLE LISDESBPBL.MASTER_ROLE_PETUGAS (
    ID_ROLE        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME       VARCHAR2(50),
    ROLE_NAME      VARCHAR2(50), -- contoh: PETUGAS_INPUT, SUPERVISOR, ADMIN
    UNITUPI        VARCHAR2(5),
    UNITAP         VARCHAR2(5),
    UNITUP         VARCHAR2(5),
    KD_PROV        VARCHAR2(5),
    KD_KAB         VARCHAR2(5),
    KD_KEC         VARCHAR2(5),
    KD_KEL         VARCHAR2(5),
    STATUS         VARCHAR2(1) DEFAULT 'A', -- aktif/nonaktif
    CREATED_AT     DATE DEFAULT SYSDATE
);
